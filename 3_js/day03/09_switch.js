//练习：获取星期的值 0~6，星期日~星期六
//根据星期的状态码打印对应中文形式
//声明变量保存星期代码

var val=7;
switch (val){
case 0:
	console.log('星期日');
	break;
case 1:
	console.log('星期一');
	break;
case 2:
	console.log('星期二');
	break;
case 3:
	console.log('星期三');
	break;
case 4:
	console.log('星期四');
	break;
case 5:
	console.log('星期五');
	break;
case 6:
	console.log('星期六');
	break;
default :
	console.log('错误');
}






















